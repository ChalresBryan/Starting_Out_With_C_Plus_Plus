#include <iostream>
/*
Characters for the ASCII Codes
Write a program that uses a loop to display the characters for the ASCII codes 0
through 127. Display 16 characters on each line.
*/

int main(){
    
    for (int i{0}; i < 100; ){
        std::cout << i << '\n';
        i = i+5;
    }

    return 0;
}






